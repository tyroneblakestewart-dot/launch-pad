#!/usr/bin/env node
// Regenerates lib/hoodlums-token-factory-artifact.ts from the Hardhat compile
// output. Run after `npm install` and `npm run contracts:compile`:
//
//   npm run contracts:compile
//   npm run contracts:factory-artifact
//
// Only the compiled bytecode is pulled from the Hardhat artifact; the ABI
// below is hand-maintained and intentionally limited to the constructor plus
// the read functions the factory-setup panel needs (owner, feeRecipient,
// launchFee, launchCount). Keep it in sync with lib/hoodlums-token-factory-artifact.ts.

import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const ARTIFACT_PATH = path.join(
  process.cwd(),
  "artifacts",
  "contracts",
  "HoodlumsTokenFactory.sol",
  "HoodlumsTokenFactory.json",
);
const OUTPUT_PATH = path.join(process.cwd(), "lib", "hoodlums-token-factory-artifact.ts");

async function main() {
  let artifact;
  try {
    artifact = JSON.parse(await readFile(ARTIFACT_PATH, "utf8"));
  } catch (error) {
    throw new Error(
      `Could not read ${ARTIFACT_PATH}. Run \`npm run contracts:compile\` first.\n${error.message}`,
    );
  }

  const bytecode = artifact.bytecode;
  if (!bytecode || bytecode === "0x") {
    throw new Error("Compiled bytecode is empty. Run `npm run contracts:compile` first.");
  }

  const output = `// Generated by scripts/generate-hoodlums-token-factory-artifact.mjs from
// contracts/HoodlumsTokenFactory.sol. Do not hand-edit the bytecode below;
// regenerate with:
//   npm run contracts:compile
//   npm run contracts:factory-artifact
import { parseAbi } from "viem";

/**
 * ABI + creation bytecode for contracts/HoodlumsTokenFactory.sol.
 *
 * Only the constructor and the read functions the factory-setup panel needs
 * (owner, feeRecipient, launchFee, launchCount) are included here — see
 * contracts/HoodlumsTokenFactory.sol for the full contract surface.
 * launchToken() is intentionally omitted: public launches are not routed
 * through the factory yet.
 */
export const HOODLUMS_TOKEN_FACTORY_ABI = parseAbi([
  "constructor(address initialOwner, address initialFeeRecipient, uint256 initialLaunchFee)",
  "function owner() view returns (address)",
  "function feeRecipient() view returns (address)",
  "function launchFee() view returns (uint256)",
  "function launchCount() view returns (uint256)",
]);

export const HOODLUMS_TOKEN_FACTORY_BYTECODE = "${bytecode}" as \`0x\${string}\`;

export const HOODLUMS_TOKEN_FACTORY_ARTIFACT_READY =
  HOODLUMS_TOKEN_FACTORY_BYTECODE.length > 2;
`;

  await writeFile(OUTPUT_PATH, output);
  console.log(
    `Wrote ${path.relative(process.cwd(), OUTPUT_PATH)} (bytecode length ${bytecode.length}).`,
  );
}

main().catch((error) => {
  console.error(error.message);
  process.exitCode = 1;
});
